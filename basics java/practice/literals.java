public class literals {
    public static void main(String a[]){

        int g = 0b100;          // printing binary numbers with base 2 right
        int q = 90;             // this is a decimalnumber 
        int r = 0x1190;         //printing the haxa decimal number
        int b = 1_00_00_000;     // we can put underscore between the numbers  for our understanding
        double v = 12e10;       // // doubles helps to store the large numbers with e 
        long t = 2344665;      // long also used as same as double
        System.out.println(g);      
        System.out.println(q);
        System.out.println(0xa);
        System.out.println(r);
        System.out.println(v);
        System.out.println(b);
        char n='h';
        n++;                         // this is increasing the character value by one means its 
        System.out.println(n); 
        System.out.println(t)  ;   // printing the next alphabet , i.e we can expect the output as 'i'
    }
    
}

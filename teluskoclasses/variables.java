public class variables {
    public static void main(String arg[]){
        int n1=3;                  // during initiation datatype should be mentioned 
        int n2=5;                  // int for integers  
        int res=n1+n2;             
        char c = 'h';              // char for single character
        String s ="im kira";       // String for strings  with 'cappital S'
        System.out.println(c);     // semicolon after every print statement
        System.out.println(s);     // semicolon after initializing variables
        System.out.println(res);
        System.out.println(3+2); // ln stands for newline
    }
    
}
// strings always should be in double quotes 
// characters should be in single quote
